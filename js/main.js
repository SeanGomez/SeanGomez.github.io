$(document).ready(function(){

	$('#secondParagraph').hide();



	$('#Paragraph').click(function(){
		$('#Paragraph').fadeOut(800);
		$('#secondParagraph').fadeIn(1000);
	});
	
	$('#secondParagraph').click(function(){
		$('#secondParagraph').fadeOut(800);
		$('#Paragraph').fadeIn(1000);
	});


});






















